#include "ft_printf.h"

char	*ft_strdup(const char *s1)
{
	size_t	size;
	char	*str;
	int		i;

	i = 0;
	size = ft_strlen(s1);
	str = (char*)malloc((size + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
